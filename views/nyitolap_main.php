<article>
    <p>
    <h4><b>A szelektív hulladékgyűjtés szabályai:</b></h4>
    </p>
    <p>
        Ahhoz, hogy a hulladékot újra lehessen hasznosítani, az eltérő
        anyagú hulladékokat külön-külön kell gyűjteni, így a fémet,
        az üveget, a műanyagot, a papírt, a szerves eredetű,
        illetve veszélyes hulladékot, de a lomot, a textil-,
        az elektromos- és elektronikai hulladékot is.
    </p>

    <ol>
        <li>
            A hulladékot tisztán (kiöblítve, letörölve, a szennyezett részeket eltávolítva)
            helyezzük a megfelelő tartályba! Ha szennyezett hulladék kerül a gyűjtőbe,
            az egyrészt fertőzésveszéllyel járhat, másrészt beszennyezheti a többi hulladékot is,
            így veszélyeztetve az újrahasznosított anyag minőségét.
        </li>
        <li>
            A nagyobb hulladékokat (pl. PET palack) nyomjuk össze! Így helyet takarítunk
            meg és több hulladék fér el a konténerben.
        </li>
        <li>
            A kupakokat csavarjuk le, a fedőket vegyük le és azokat külön dobjuk a szelektív gyűjtőbe!
        </li>
        <li>
            A tejes és gyümölcsleves dobozokat öblítsük el és helyezzük a megfelelő tárolóba! Budapesten
            ezeket is a papírkonténerbe kell tenni, a kupakokat pedig a műanyaghulladékhoz. Az ország más
            részein több helyen a dobozokat is a műanyaghulladékokkal együtt kell gyűjteni, ezért minden
            esetben a helyi hulladékkezelő szolgáltatónál érdemes érdeklődnünk.
        </li>
        <li>
            Azokat a termékeket, amik egyszerre tartalmaznak papírt, fémet és/vagy műanyagot,
            bontsuk elemeire és elemenként helyezzük a megfelelő tartályba!
        </li>
        <li>
            A papírhulladékot ne műanyagzacskóban dobjuk ki és ne kössük át madzaggal vagy műanyag zsinórral!
        </li>
        <li>
            Attól még, hogy valami műanyag, nem biztos, hogy újrahasznosítható! Figyeljünk a csomagoláson látható jelzésekre!
        </li>
        <li>
            Azokat a hulladékokat, amiket nem lehet az otthoni hulladékgyűjtőbe tenni, vigyük el a legközelebbi
            szelektív hulladékgyűjtő szigetre vagy hulladékudvarba!
        </li>
    </ol>
