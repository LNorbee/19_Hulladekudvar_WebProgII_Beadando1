    <p>
    <h4><b>Szelektív hulladékgyűjtés</b></h4>
    </p>

    <p><b>"A környezettudatos életmód a szelektív hulladékgyűjtéssel kezdődik."</b>
    </p>
    <p>
        Már ezzel az apró lépéssel is hozzá tudunk járulni bolygónk
        (és nem mellesleg az emberiség) megmentéséhez, és egy zöldebb,
        fenntarthatóbb, a környezetünkkel harmóniában megvalósuló élet
        kialakításához.
        A szelektív hulladékgyűjtés a körforgásos gazdaság irányába való
        elmozdulás egyik fontos alappillére. Az elmúlt években az Európai Unió
        egyre határozottabb lépéseket tesz a lineáris gazdasági modellről a
        körkörös gazdasági modellre történő váltásra.
    </p>
