    <p>
        A <b>Hulladék Udvar Kft.</b> a lakosság környezetszemléletű
        tudat-formálását célozva hulladékudvarokat üzemeltet.
    </p>
    <p>
        Hulladékudvarainkba azokat a hulladékokat érdemes elvinni, amelyeket a házhoz menő
        rendszerben nem szállítanak el, vagy nem adhatók le gyűjtőszigeteken.
        Ilyenek például a veszélyes hulladékok (pl. festékek, bontási törmelék,
        akkumulátorok), az elektronikai hulladékok (pl. háztartási kis- és
        nagygépek, fénycsövek) vagy a háztartásban keletkező lomok.
    </p>
    <p>
        Arról, hogy milyen típusú hulladékot mely hulladékudvarunkba lehet elvinni,
        a <b><a href="regisztracio" class= "text-success">regisztrációt</a></b> követően kap tájékoztatást
    </p>

</article>
